# Define a function called city_country that takes two parameters: city and country
def city_country(city, country):
    # Print the city and country in the format "city, country" with double quotes
    print(f'"{city}, {country}"')

# Call the function with different city and country pairs
city_country('a', 'b')  # Output: "a, b"
city_country('c', 'd')  # Output: "c, d"
city_country('e', 'f')  # Output: "e, f"
