# Dictionary of programming terms and their meanings
glossary = {
    'variable': 'A name used to store a value in a program.',
    'list': 'A collection of items in a particular order.',
    'dictionary': 'A collection of key-value pairs.',
    'loop': 'A way to repeat a block of code multiple times.',
    'function': 'A named block of code that performs a specific task.'
}

# Print each word and its meaning
for word, meaning in glossary.items():
    print(f"{word.capitalize()}:\n  {meaning}\n")
